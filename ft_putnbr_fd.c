#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		dig;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= (-1);
	}
	if (n >= 0 && n <= 9)
	{
		dig = n + '0';
		write(fd, &dig, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
