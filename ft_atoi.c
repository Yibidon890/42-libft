int	ft_atoi(char const *str)
{
	int		i;
	int		minus;
	int		v;

	minus = 1;
	v = 0;
	i = 0;
	while (str[i] == '\n' || str[i] == ' ' || str[i] == '\r'
		|| str[i] == '\f' || str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] != '\0' && (str[i] == '+' || str[i] == '-'))
	{
		if (str[i] == '-')
			minus *= -1;
		i++;
	}
	while ((str[i] != '\0') && (str[i] >= '0' && str[i] <= '9'))
	{
		v = v * 10 + (str[i] - '0');
		i++;
	}
	v *= minus;
	return (v);
}
