#include "libft.h"

char	*ft_strdup(char const *s)
{
	int		i;
	int		len;
	char	*cpy;

	len = ft_strlen(s);
	cpy = malloc(sizeof(*cpy) * len + 1);
	if (!cpy)
		return (NULL);
	i = 0;
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
